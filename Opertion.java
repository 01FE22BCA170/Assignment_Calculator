public class Opertion {
    // Returns the square of a number
    public float square(float opr1) {
        return (opr1 * opr1);
    }

    // Returns the cube of a number
    public float cube(float opr1) {
        return (opr1 * opr1 * opr1);
    }

    // Returns the modulus of two numbers
    public float modulus(float opr1, float opr2) {
        return (opr1 % opr2);
    }
}
